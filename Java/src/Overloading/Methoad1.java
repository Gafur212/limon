package Overloading;

public class Methoad1 {
	
	
	
	
	
	public void div(int a, int b) {
		
		System.out.println(a+b);
		
		}
	
	public void div(double a, double c) {
		System.out.println(a-c);
	}
	
	public void div(int a, int b,int c) {
		System.out.println(a+b+c);
		
	}
	
	public void div() {
		
		System.out.println("print nothing to div");
	}
	
	public void div(byte a, byte b) {
		System.out.println(a*b);
		
	}
	
	public void div(boolean ture) {
		System.out.println(ture);
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	

}
